def is_valid_sudoku(board):

    function_map = {
        0: (lambda i, j: (i, j)),
        1: (lambda i, j: (j, i)),
        2: (lambda i, j: ((j // 3) + (i % 3) * 3, (j % 3) + (i // 3) * 3)),
    }

    for type in range(3):
        transform = function_map[type]
        for i in range(9):
            lst = []
            for j in range(9):
                a, b = transform(i, j)
                val = board[a][b]
                if val == '.':
                    continue

                if val not in lst:
                    lst.append(val)
                else:
                    return False

    return True


